#!/bin/bash 

rm -rf GRN* syn* pygrtstats* *.png

# -----------------------------------------------------------------
# BEGIN GRN
# -S-1 表示输出所有频率点的核函数
# -V0.1 显式给定参考速度（用于定义波数积分上限），避免使用PTAM
# -L20 定义波数积分间隔dk
grt -Mmod1 -D0.01/0 -N500/0.02/0.8 -OGRN -R1 -V0.1 -S-1 -L20
# END GRN
# -----------------------------------------------------------------
